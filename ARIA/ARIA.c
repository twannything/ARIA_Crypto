void left_shift(byte* x, int l) {
	int k = 0;
	int r = 0;
	int i = 0;

	k = l / 8;
	r = l % 8;

	if (r == 0) {

		for (i = 0; i + k < 16; i++)
			x[i] = x[i + k];
		for (int j = i; j < 16; j++)
			x[j] = 0x00;
	}
	else {
		for (i = 0; i + k < 15; i++)
			x[i] = (x[i + k] << r) ^ (x[i + 1 + k] >> (8 - r));
		x[i] = x[i + k] << r;
		for (int j = i+1; j < 16; j++)
			x[j] = 0x00;
	}
}

